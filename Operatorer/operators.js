//*******EN SAMMANFATTNINGA AV OPERATORER*******

//***MATTEOPERATORER***

// 1. Addition (+):
let sum = 5 + 3; // Resultat: 8

// 2. Subtraktion (-):
let difference = 10 - 4; // Resultat: 6

// 3. Multiplikation (*):
let product = 3 * 6; // Resultat: 18

// 4. Division (/):
let quotient = 15 / 3; // Resultat: 5

//-----------------------------------------------

//***JÄMFÖRELSEOPERATORER***

// 1. Mindre än (<):
let isLessThan = 5 < 8; // Resultat: true

// 2. Större än (>):
let isGreaterThan = 10 > 5; // Resultat: true

// 3. Mindre än eller lika med (<=):
let lessThanOrEqual = 4 <= 4; // Resultat: true

// 4. Större än eller lika med (>=):
let greaterThanOrEqual = 9 >= 8; // Resultat: true

// 5. Likhet (==):
let isEqual = 7 == "7"; // Resultat: true (jämförelse efter typomvandling)

// 6. Olika (!=):
let isNotEqual = 10 != 5; // Resultat: true

//-----------------------------------------------------------------

//***ÖKA, MINSKA-OPERATORER:***

// 1. Inkrement (++):
let x = 5;
x++;
console.log(x); // Resultat: 6

// 2. Dekrement (--):
let y = 8;
y--;
console.log(y); // Resultat: 7

//----------------------------------------------------------

//***TILLDELNINGSOPERATORER:***

// 1. Tilldelning (=):
let a = 10;

// 2. Tilldela och addera (+=):
let b = 5;
b += 3; // b blir 8 (5 + 3)

// 3. Tilldela och subtrahera (-=):
let c = 12;
c -= 4; // c blir 8 (12 - 4)

// 4. Tilldela och multiplicera (*=):
let d = 3;
d *= 6; // d blir 18 (3 * 6)

// 5. Tilldela och dividera (/=):
let e = 20;
e /= 5; // e blir 4 (20 / 5)

//-----------------------------------------------------------

//***NEGATIONSOPERATORER:***
// 1. Negation (!):
let isTrue = true;
let isNotTrue = !isTrue; // Resultat: false
