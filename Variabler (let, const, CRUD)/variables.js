// Använda let för att deklarera en variabel
let minVariabel = "Hej, världen!";
console.log(minVariabel);

// Tilldela värdet till en variabel som deklarerats med let
minVariabel = "Hej, GPT-3!";
console.log(minVariabel);

// Använda const för att deklarera en konstant variabel
const pi = 3.14;
console.log(pi);

